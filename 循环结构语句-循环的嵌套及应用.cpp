//【利用嵌套打印一组星图】

#include<iostream>
using namespace std;
int main()
{
	//利用嵌套打印一组星图
	int i;
	//外行执行一次 内行执行一周
	for (i = 0; i < 10; i++)
	{
		//内行循环
		for (int j = 0; j < 10; j++)  //打印多少个（执行j=1 2 3 4 5 6 7 8 9）
		{
			cout << "!";
		}
		cout << endl;  //换新行
	}
	system("pause");
	return 0;
}

