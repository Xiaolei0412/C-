//【打印9*9乘法表】
#include<iostream>
using namespace std;
int main()
{
	//乘法口诀表
	int i, j;
	//外循环一次，内循环一圈。
	for (i = 1; i <= 9; i++)
	{
		//内循环
		for (j = 1; j <= i; j++)      
		{
			cout << j << "*" << i << "=" << j*i<<"	"; //输出j*i的值 
		}
		cout << endl; 
	}
	system("pause");  
	return 0;
}
